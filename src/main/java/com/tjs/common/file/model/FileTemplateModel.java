package com.tjs.common.file.model;

import com.tjs.common.model.IdentifierModel;

/**
 * This is model for templates.
 * 
 * @author Nirav.Shah
 * @version 1.0
 * @since 28/08/2018
 */

public class FileTemplateModel extends IdentifierModel {

	private static final long serialVersionUID = -2011646432378386665L;
	private String name;
	private String fileId;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getFileId() {
		return fileId;
	}
	public void setFileId(String fileId) {
		this.fileId = fileId;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((fileId == null) ? 0 : fileId.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		FileTemplateModel other = (FileTemplateModel) obj;
		if (fileId == null) {
			if (other.fileId != null)
				return false;
		} else if (!fileId.equals(other.fileId))
			return false;
		return true;
	}
}
